<?php

$op = 1;

// Switch/Case do PHP
switch ($op) {
    case 1:
        echo "Cadastrar";
        break;
    case 2:
        echo "Atualizar";
        break;
    case 3:
        echo "Ler";
        break;
    case 4:
        echo "Excluir";
        break;
    default:
        echo "Opção Inválida";
        break;
}